namespace Encapsulation
{
    public class AccessModifiers
    {
        // •	private: Sadece aynı sınıf içerisinden erişilebilir. Dışarıdan erişim tamamen kısıtlanır.
        // •	protected: Aynı sınıf ve ondan türeyen sınıflar (alt sınıflar) erişebilir.
        // •	internal: Aynı proje veya assembly içerisinden erişilebilir.
        // •	public: Her yerden erişime açıktır.
        // •	protected internal: Aynı projedeki sınıflardan veya bu sınıfı miras alan sınıflardan erişilebilir.
        // •	private protected: Aynı projede bulunan ve miras alan sınıflar tarafından erişilebilir.


    }
}